    private void savebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebuttonActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            String deptname = txtdepartment.getSelectedItem().toString();
            String code = txtsubjectcode.getText();
            String subject = txtsubject.getText();

            pst = cdb.conn.prepareStatement("insert into subject(subjectcode,dept_name,subjectname)values(?,?,?)");
            pst.setString(1, code);
            pst.setString(2, deptname);
            pst.setString(3, subject);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Subject details added successfully...");
            Subject_Load();

            txtsubject.setText("");
            txtdepartment.setSelectedIndex(0);
            txtsubjectcode.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_savebuttonActionPerformed

    private void editbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbuttonActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            d = (DefaultTableModel) jTable1.getModel();
            int selectIndex = jTable1.getSelectedRow();

            String id = d.getValueAt(selectIndex, 0).toString();
            int a = Integer.parseInt(id);
            String deptname = txtdepartment.getSelectedItem().toString();
            String code = txtsubjectcode.getText();
            String subject = txtsubject.getText();

            pst = cdb.conn.prepareStatement("update subject set subjectcode=?,dept_name=?,subjectname=? where id=?");

            pst.setString(1, code);
            pst.setString(2, deptname);
            pst.setString(3, subject);
            pst.setInt(4, a);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "subject details edited successfully...");
            savebutton.setEnabled(true);

            txtsubject.setText("");
            txtdepartment.setSelectedIndex(0);
            txtsubjectcode.setText("");

            txtsubjectcode.requestFocus();

            Subject_Load();

        } catch (SQLException ex) {
            Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_editbuttonActionPerformed

